            w: 0,
            h: 0,
            x: 0,
            y: 0,
            scale: 0,
            angle: 0
        }
    ]
}

const drawComplete = () => {
    loading.value = false
}

const previewShow = ref<boolean>(false)
const previewUrl = ref<string>('')
